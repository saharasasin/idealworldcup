package com.ideal.worldcup.dto;

public class CommonFileVO {
	private int code;
	private int fk_code;
	private String origin_name;
	private String thumb_name;
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public int getFk_code() {
		return fk_code;
	}
	public void setFk_code(int fk_code) {
		this.fk_code = fk_code;
	}
	public String getOrigin_name() {
		return origin_name;
	}
	public void setOrigin_name(String origin_name) {
		this.origin_name = origin_name;
	}
	public String getThumb_name() {
		return thumb_name;
	}
	public void setThumb_name(String thumb_name) {
		this.thumb_name = thumb_name;
	}
	
	
}
